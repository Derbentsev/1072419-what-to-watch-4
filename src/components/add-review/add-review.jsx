import UserLogo from '@components/user-logo/user-logo.connect';


const ReviewParams = {
  MIN_LENGTH: 50,
  MAX_LENGTH: 400,
};

const toggleElementsDisabled = (elements) => {
  [...elements].forEach((element) => {
    element.disabled = element.disabled ? false : true;
  });
};

const handleFormSubmit = (evt) => {
  evt.preventDefault();

  const formElements = evt.target.elements;
  toggleElementsDisabled(formElements);

  const reviewText = evt.target.getElementsByClassName(`add-review__textarea`)[0].value;
  if (reviewText.length < ReviewParams.MIN_LENGTH || reviewText.length > ReviewParams.MAX_LENGTH) {
    toggleElementsDisabled(formElements);
    return false;
  }

  toggleElementsDisabled(formElements);
  return true;
};


